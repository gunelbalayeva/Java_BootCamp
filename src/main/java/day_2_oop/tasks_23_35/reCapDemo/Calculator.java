package day_2_oop.tasks_23_35.reCapDemo;

public class Calculator {
    public int topla(int a, int b) {
        return a + b;
    }

    public int cixma(int a, int b) {
        return a - b;
    }

    public int vurma(int a, int b) {
        return a * b;
    }

    public int bolme(int a, int b) {
        return a / b;
    }
}
